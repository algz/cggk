public class SupplyMaterialVo {

	private String id;
	private String contractCode;
	private String contractName;
	private String num;
	private String name;
	private String scale;
	private String price;
	private String loton;
	private String count;
	private String deliveryDate;
	private String personOfPass;

	public String getId() {
		return id;
	}

	public void setId(String id) {
		this.id = id;
	}

	public String getContractCode() {
		return contractCode;
	}

	public void setContractCode(String contractCode) {
		this.contractCode = contractCode;
	}

	public String getContractName() {
		return contractName;
	}

	public void setContractName(String contractName) {
		this.contractName = contractName;
	}

	public String getNum() {
		return num;
	}

	public void setNum(String num) {
		this.num = num;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getScale() {
		return scale;
	}

	public void setScale(String scale) {
		this.scale = scale;
	}

	public String getPrice() {
		return price;
	}

	public void setPrice(String price) {
		this.price = price;
	}

	public String getLoton() {
		return loton;
	}

	public void setLoton(String loton) {
		this.loton = loton;
	}

	public String getCount() {
		return count;
	}

	public void setCount(String count) {
		this.count = count;
	}

	public String getDeliveryDate() {
		return deliveryDate;
	}

	public void setDeliveryDate(String deliveryDate) {
		this.deliveryDate = deliveryDate;
	}

	public String getPersonOfPass() {
		return personOfPass;
	}

	public void setPersonOfPass(String personOfPass) {
		this.personOfPass = personOfPass;
	}

}
