package com.sysware.customize.hd.investment.purchaseRequest.PEsummary.vo;

public class SpecialProjectVo {

	private String id;
	private String projectname;//项目名称
	private String projectnum;//项目编号
	private String createperson;//创建者
	private String contractmoney;//合同金额
	private String costunit;//单位
	private String createtime;//创建时间
	private String approvalstate;//审批状态
	private String remark;	//备注
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getProjectname() {
		return projectname;
	}
	public void setProjectname(String projectname) {
		this.projectname = projectname;
	}
	public String getProjectnum() {
		return projectnum;
	}
	public void setProjectnum(String projectnum) {
		this.projectnum = projectnum;
	}
	public String getCreateperson() {
		return createperson;
	}
	public void setCreateperson(String createperson) {
		this.createperson = createperson;
	}
	public String getContractmoney() {
		return contractmoney;
	}
	public void setContractmoney(String contractmoney) {
		this.contractmoney = contractmoney;
	}
	public String getCostunit() {
		return costunit;
	}
	public void setCostunit(String costunit) {
		this.costunit = costunit;
	}
	public String getCreatetime() {
		return createtime;
	}
	public void setCreatetime(String createtime) {
		this.createtime = createtime;
	}
	public String getApprovalstate() {
		return approvalstate;
	}
	public void setApprovalstate(String approvalstate) {
		this.approvalstate = approvalstate;
	}
	public String getRemark() {
		return remark;
	}
	public void setRemark(String remark) {
		this.remark = remark;
	}
	
	
}
